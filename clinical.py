def build_preprocessor(numeric_cols, binary_cols, categorical_cols,
                        ordinal_cols=None):
    """
    ColumnTransformer:
      Numeric     → median imputation + StandardScaler
      Binary      → most-frequent imputation, NO scaling
      Categorical → 'Unknown' fill + OneHotEncoder
      Ordinal num → most-frequent imputation + StandardScaler
    """
    ordinal_cols = ordinal_cols or []
    transformers = []

    if numeric_cols:
        num_pipe = Pipeline([
            ("imputer", SimpleImputer(strategy="median")),
            ("scaler",  StandardScaler()),
        ])
        transformers.append(("num", num_pipe, numeric_cols))

    if binary_cols:
        bin_pipe = Pipeline([
            ("imputer", SimpleImputer(strategy="most_frequent")),
        ])
        transformers.append(("bin", bin_pipe, binary_cols))

    if categorical_cols:
        cat_pipe = Pipeline([
            ("imputer", SimpleImputer(strategy="constant",
                                      fill_value="Unknown")),
            ("ohe",     OneHotEncoder(handle_unknown="ignore",
                                      sparse_output=False)),
        ])
        transformers.append(("cat", cat_pipe, categorical_cols))

    if ordinal_cols:
        ord_pipe = Pipeline([
            ("imputer", SimpleImputer(strategy="most_frequent")),
            ("scaler",  StandardScaler()),
        ])
        transformers.append(("ord", ord_pipe, ordinal_cols))

    return ColumnTransformer(transformers=transformers, remainder="drop",
                             verbose_feature_names_out=False)


def build_model_pipeline(preprocessor, model):
    return Pipeline([("preprocessor", preprocessor), ("classifier", model)])


def build_model_pipeline_fs(preprocessor, model, model_name, k=9):
    """Pipeline with optional SelectKBest for Logistic Regression."""
    steps = [("preprocessor", preprocessor)]
    if model_name == "Logistic Regression":
        steps.append(("feature_selector",
                       SelectKBest(score_func=f_classif, k=k)))
    steps.append(("classifier", model))
    return Pipeline(steps)


