def plot_roc_cv(fold_data, model_name="Model", ax=None):
    """
    Mean ROC curve with ±1 std shading + individual fold curves.
    """
    tprs     = fold_data["tprs"]
    mean_fpr = fold_data["mean_fpr"]
    s        = fold_data["summary"]

    # FIX: clipping for numerical stability
    mean_tpr = np.clip(tprs.mean(axis=0), 0, 1)
    mean_tpr[-1] = 1.0
    std_tpr  = tprs.std(axis=0)

    own_fig = ax is None
    if own_fig:
        fig, ax = plt.subplots(figsize=(6, 5))

    # Individual folds
    for fp_d in fold_data["fold_predictions"]:
        fpr_f, tpr_f, _ = roc_curve(fp_d["y_true"], fp_d["y_prob"])
        ax.plot(fpr_f, tpr_f, color="#a0c4ff", lw=0.8, alpha=0.4)

    # Mean ROC
    auc_mean, auc_std = s["AUC"]["mean"], s["AUC"]["std"]
    ax.plot(mean_fpr, mean_tpr, color="#1a6faf", lw=2.5,
            label=f"Mean ROC (AUC = {auc_mean:.3f} ± {auc_std:.3f})")

    # Std shading
    ax.fill_between(mean_fpr,
                    np.clip(mean_tpr - std_tpr, 0, 1),
                    np.clip(mean_tpr + std_tpr, 0, 1),
                    color="#1a6faf", alpha=0.15, label="± 1 std dev")

    # Baseline
    ax.plot([0, 1], [0, 1], linestyle="--", color="#888888",
            lw=1.2, label="Random Classifier")

    ax.set_xlim([-0.02, 1.02])
    ax.set_ylim([-0.02, 1.05])
    ax.set_xlabel("False Positive Rate", fontsize=11)
    ax.set_ylabel("True Positive Rate", fontsize=11)
    ax.set_title(f"ROC Curve — {model_name}", fontsize=12, fontweight="bold")
    ax.legend(loc="lower right", fontsize=9)
    ax.grid(True, linestyle="--", alpha=0.4)

    if own_fig:
        plt.tight_layout()
        plt.show()
        return fig


