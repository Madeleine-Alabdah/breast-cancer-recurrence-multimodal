def evaluate_model_cv(model, X, y, preprocessor=None,
                      n_splits=5, random_state=RANDOM_STATE,
                      model_name="Model"):

    if preprocessor is not None:
        estimator = Pipeline([("preprocessor", preprocessor),
                              ("classifier",   model)])
    else:
        estimator = model

    cv = StratifiedKFold(n_splits=n_splits, shuffle=True,
                         random_state=random_state)

    fold_metrics = {k: [] for k in
                    ["AUC", "AP", "Accuracy", "Precision",
                     "Recall", "Specificity", "F1"]}

    mean_fpr = np.linspace(0, 1, 100)
    mean_recall_grid = np.linspace(0, 1, 100)

    tprs = []
    precisions_interp = []
    fold_predictions = []

    X = X.reset_index(drop=True)
    y = y.reset_index(drop=True)

    for train_idx, val_idx in cv.split(X, y):
        X_tr, X_val = X.iloc[train_idx], X.iloc[val_idx]
        y_tr, y_val = y.iloc[train_idx], y.iloc[val_idx]

        est_fold = clone(estimator)
        est_fold.fit(X_tr, y_tr)

        y_prob = est_fold.predict_proba(X_val)[:, 1]
        y_pred = est_fold.predict(X_val)

        # =========================
        # Metrics (FIXED)
        # =========================
        tn, fp, fn, tp = confusion_matrix(y_val, y_pred).ravel()
        specificity = tn / (tn + fp) if (tn + fp) > 0 else 0.0

        # Handle edge case (single class)
        if len(np.unique(y_val)) > 1:
            auc = roc_auc_score(y_val, y_prob)
            ap  = average_precision_score(y_val, y_prob)
        else:
            auc = 0.5
            ap  = 0.0

        fold_metrics["AUC"].append(auc)
        fold_metrics["AP"].append(ap)
        fold_metrics["Accuracy"].append(accuracy_score(y_val, y_pred))
        fold_metrics["Precision"].append(
            precision_score(y_val, y_pred, zero_division=0))
        fold_metrics["Recall"].append(
            recall_score(y_val, y_pred, zero_division=0))
        fold_metrics["Specificity"].append(specificity)
        fold_metrics["F1"].append(
            f1_score(y_val, y_pred, zero_division=0))

        # =========================
        # ROC (IMPROVED)
        # =========================
        fpr_f, tpr_f, _ = roc_curve(y_val, y_prob)
        tpr_interp = np.interp(mean_fpr, fpr_f, tpr_f)
        tpr_interp[0] = 0.0
        tpr_interp[-1] = 1.0
        tprs.append(tpr_interp)

        # =========================
        # PR (FIXED — IMPORTANT)
        # =========================
        prec_f, rec_f, _ = precision_recall_curve(y_val, y_prob)

        # Sort recall correctly
        order = np.argsort(rec_f)
        rec_sorted = rec_f[order]
        prec_sorted = prec_f[order]

        prec_interp = np.interp(mean_recall_grid,
                               rec_sorted,
                               prec_sorted)

        precisions_interp.append(prec_interp)

        fold_predictions.append({
            "y_true": y_val.values,
            "y_prob": y_prob,
            "y_pred": y_pred,
        })

    # =========================
    # Aggregate
    # =========================
    summary_dict = {}
    rows = []

    for metric, values in fold_metrics.items():
        arr = np.array(values)

        summary_dict[metric] = {
            "mean": round(float(arr.mean()), 4),
            "std":  round(float(arr.std()), 4),
            "min":  round(float(arr.min()), 4),
            "max":  round(float(arr.max()), 4),
            "per_fold": arr.tolist(),
        }

        rows.append({
            "Metric": metric,
            "Mean": summary_dict[metric]["mean"],
            "Std":  summary_dict[metric]["std"],
            "Min":  summary_dict[metric]["min"],
            "Max":  summary_dict[metric]["max"],
        })

    stats_df = pd.DataFrame(rows)

    # =========================
    # Print
    # =========================
    print(f"\n  📊 {model_name} — {n_splits}-Fold Cross-Validation Results")
    print(f"  {'─' * 65}")
    for metric, s in summary_dict.items():
        print(f"  {metric + ':':<14} "
              f"{s['mean']:.3f} ± {s['std']:.3f}  "
              f"(min={s['min']:.3f}, max={s['max']:.3f})")
    print(f"  {'─' * 65}")

    fold_data = {
        "tprs": np.array(tprs),
        "mean_fpr": mean_fpr,
        "precisions_interp": np.array(precisions_interp),
        "mean_recall_grid": mean_recall_grid,
        "fold_predictions": fold_predictions,
        "fold_metrics": fold_metrics,
        "summary": summary_dict,
        "stats_df": stats_df,
    }

    return summary_dict, stats_df, fold_data

