def tune_threshold(y_true, y_prob, model_label="Model"):
    thresholds = np.arange(0.05, 0.61, 0.025)
    rows = []

    for t in thresholds:
        y_pred_t = (y_prob >= t).astype(int)
        tn, fp, fn, tp = confusion_matrix(y_true, y_pred_t).ravel()

        rows.append({
            "Threshold":   round(t, 3),
            "Precision":   round(precision_score(y_true, y_pred_t, zero_division=0), 4),
            "Sensitivity": round(recall_score(y_true, y_pred_t, zero_division=0), 4),
            "Specificity": round(tn / (tn + fp) if (tn + fp) > 0 else 0.0, 4),
            "F1":          round(f1_score(y_true, y_pred_t, zero_division=0), 4),
        })

    sweep = pd.DataFrame(rows)
    best_idx = sweep["F1"].idxmax()
    best_t = sweep.loc[best_idx, "Threshold"]

    print(f"\n  [{model_label}] Optimal threshold: {best_t}  "
          f"F1={sweep.loc[best_idx,'F1']}  "
          f"Precision={sweep.loc[best_idx,'Precision']}  "
          f"Sensitivity={sweep.loc[best_idx,'Sensitivity']}  "
          f"Specificity={sweep.loc[best_idx,'Specificity']}")

    return best_t, sweep


def plot_threshold_sweep(sweep, best_t, title="Threshold Tuning"):
    fig, ax = plt.subplots(figsize=(8, 4))

    ax.plot(sweep["Threshold"], sweep["F1"], label="F1", lw=2)
    ax.plot(sweep["Threshold"], sweep["Precision"], label="Precision", lw=2)
    ax.plot(sweep["Threshold"], sweep["Sensitivity"], label="Sensitivity", lw=2)
    ax.plot(sweep["Threshold"], sweep["Specificity"], label="Specificity", lw=2)

    ax.axvline(best_t, linestyle="--", lw=1.5,
               label=f"Optimal τ={best_t}")

    ax.set_xlabel("Decision Threshold (τ)", fontsize=11)
    ax.set_ylabel("Score", fontsize=11)
    ax.set_title(title, fontsize=12, fontweight="bold")
    ax.legend()
    ax.set_ylim(0, 1.05)

    plt.tight_layout()
    plt.show()

    return fig


