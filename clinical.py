def plot_threshold_metrics_cv(fold_data, model_name="Model"):
    """
    Threshold vs. F1 / Precision / Recall / Specificity
    """
    y_true_all = np.concatenate(
        [d["y_true"] for d in fold_data["fold_predictions"]])
    y_prob_all = np.concatenate(
        [d["y_prob"] for d in fold_data["fold_predictions"]])

    thresholds = np.linspace(0.05, 0.95, 91)
    f1s, precs, recs, specs = [], [], [], []

    for t in thresholds:
        y_pred_t = (y_prob_all >= t).astype(int)
        tn, fp, fn, tp = confusion_matrix(
            y_true_all, y_pred_t, labels=[0, 1]).ravel()

        f1s.append(f1_score(y_true_all, y_pred_t, zero_division=0))
        precs.append(precision_score(y_true_all, y_pred_t, zero_division=0))
        recs.append(recall_score(y_true_all, y_pred_t, zero_division=0))
        specs.append(tn / (tn + fp) if (tn + fp) > 0 else 0.0)

    best_t = thresholds[int(np.argmax(f1s))]

    fig, ax = plt.subplots(figsize=(8, 4))
    ax.plot(thresholds, f1s,   lw=2, label="F1-Score")
    ax.plot(thresholds, precs, lw=2, label="Precision")
    ax.plot(thresholds, recs,  lw=2, label="Recall (Sensitivity)")
    ax.plot(thresholds, specs, lw=2, label="Specificity")
    ax.axvline(best_t, color="black", linestyle="--", lw=1.5,
               label=f"Best F1 at τ = {best_t:.2f}")

    ax.set_xlabel("Decision Threshold (τ)", fontsize=11)
    ax.set_ylabel("Score", fontsize=11)
    ax.set_title(f"Threshold vs. Metrics — {model_name}",
                 fontsize=12, fontweight="bold")
    ax.legend(fontsize=9)
    ax.set_xlim(0.0, 1.0)
    ax.set_ylim(0.0, 1.05)
    ax.grid(True, linestyle="--", alpha=0.4)
    plt.tight_layout()
    plt.show()
    return fig


