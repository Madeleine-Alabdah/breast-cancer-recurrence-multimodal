def run_experiment(df, experiment_name, numeric_cols, binary_cols,
                   categorical_cols, ordinal_cols=None):

    ordinal_cols = ordinal_cols or []

    print(f"\n{'═'*65}")
    print(f"  EXPERIMENT: {experiment_name}")
    print(f"{'═'*65}")

    # ── Ensure columns exist ─────────────────────────────────────────────────
    def existing(cols):
        return [c for c in cols if c in df.columns]

    numeric_cols     = existing(numeric_cols)
    binary_cols      = existing(binary_cols)
    categorical_cols = existing(categorical_cols)
    ordinal_cols     = existing(ordinal_cols)

    all_features = numeric_cols + binary_cols + categorical_cols + ordinal_cols

    if len(all_features) == 0:
        raise ValueError("❌ No valid features found!")

    # ── Prepare data ─────────────────────────────────────────────────────────
    X = df[all_features].copy()
    y = df[TARGET_COL].copy()

    valid = y.notna()
    X, y  = X[valid], y[valid].astype(int)

    if len(X) == 0:
        raise ValueError("❌ No valid samples after filtering!")

    print(f"\n  Samples: {len(X)}")

    # Class distribution
    class_dist = (y.value_counts(normalize=True) * 100).round(2)
    print("\n  Class distribution (%):")
    print(class_dist.rename({0: "No Recurrence", 1: "Recurrence"}))

    # ── Train/Test Split ─────────────────────────────────────────────────────
    X_train, X_test, y_train, y_test = train_test_split(
        X, y,
        test_size=0.2,
        stratify=y,
        random_state=RANDOM_STATE
    )

    print(f"\n  Train: {len(X_train)} | Test: {len(X_test)}")

    # ── Preprocessing ────────────────────────────────────────────────────────
    preprocessor = build_preprocessor(
        numeric_cols, binary_cols, categorical_cols, ordinal_cols
    )

    # ── Cross-validation setup ───────────────────────────────────────────────
    cv = StratifiedKFold(
        n_splits=5,
        shuffle=True,
        random_state=RANDOM_STATE
    )

    # ── Models ───────────────────────────────────────────────────────────────
    models = {
        "Logistic Regression": LogisticRegression(
            max_iter=1000,
            class_weight="balanced",
            random_state=RANDOM_STATE
        ),
        "Random Forest": RandomForestClassifier(
            n_estimators=300,
            class_weight="balanced",
            random_state=RANDOM_STATE
        ),
    }

    if HAS_XGBOOST:
        neg_n = (y_train == 0).sum()
        pos_n = (y_train == 1).sum()

        models["XGBoost"] = XGBClassifier(
            n_estimators=300,
            scale_pos_weight=neg_n / pos_n,
            eval_metric="logloss",
            random_state=RANDOM_STATE,
            verbosity=0
        )

    print(f"\n  Models to evaluate: {list(models.keys())}")

    results       = []
    all_fold_data = {}

    # ── Training loop ────────────────────────────────────────────────────────
    for name, model in models.items():
        print(f"\n  {'─'*55}")
        print(f"  ▶ Running: {name}")
        print(f"  {'─'*55}")

        pipe = build_model_pipeline(preprocessor, model)

        metrics = evaluate_pipeline(
            pipe,
            X_train, X_test,
            y_train, y_test,
            cv,
            name
        )

        metrics["Experiment"] = experiment_name

        # Store fold data for plotting
        all_fold_data[name] = metrics.pop("_fold_data", None)

        results.append(metrics)

    # ── Overlay curves (VERY IMPORTANT for paper) ────────────────────────────
    if all_fold_data:
        print(f"\n  📊 Overlay Curves — {experiment_name}")

        plot_cv_all_models(
            all_fold_data,
            plot_type="roc",
            experiment_name=experiment_name
        )

        plot_cv_all_models(
            all_fold_data,
            plot_type="pr",
            experiment_name=experiment_name
        )

    print(f"\n✅ Experiment '{experiment_name}' completed\n")

    return results


