def compute_specificity(y_true: np.ndarray, y_pred: np.ndarray) -> float:
    """Specificity = TN / (TN + FP) derived from confusion matrix."""
    cm = confusion_matrix(y_true, y_pred)
    if cm.shape == (2, 2):
        tn, fp, _, _ = cm.ravel()
        return tn / (tn + fp) if (tn + fp) > 0 else 0.0
    return 0.0


def evaluate_fold(y_true: np.ndarray, y_proba: np.ndarray) -> dict:
    """
    Compute all seven metrics for a single CV fold or the final test set.

    Uses predict_proba outputs for AUC and AP; threshold 0.5 for the rest.

    Returns
    -------
    dict — keys: auc, ap, accuracy, precision, recall, specificity, f1
    """
    y_pred = (y_proba >= 0.5).astype(int)
    return {
        'auc'        : roc_auc_score(y_true, y_proba),
        'ap'         : average_precision_score(y_true, y_proba),
        'accuracy'   : accuracy_score(y_true, y_pred),
        'precision'  : precision_score(y_true, y_pred, zero_division=0),
        'recall'     : recall_score(y_true, y_pred, zero_division=0),
        'specificity': compute_specificity(y_true, y_pred),
        'f1'         : f1_score(y_true, y_pred, zero_division=0),
    }


def aggregate_fold_metrics(fold_metrics: list) -> dict:
    """
    Aggregate per-fold metric dicts into mean ± std (min, max, per_fold).

    Parameters
    ----------
    fold_metrics : list[dict]  — one dict per CV fold

    Returns
    -------
    agg : dict  — {metric: {mean, std, min, max, per_fold}}
    """
    metric_keys = fold_metrics[0].keys()
    agg = {}
    for key in metric_keys:
        vals = [fm[key] for fm in fold_metrics]
        agg[key] = {
            'mean'     : float(np.mean(vals)),
            'std'      : float(np.std(vals, ddof=1)),
            'min'      : float(np.min(vals)),
            'max'      : float(np.max(vals)),
            'per_fold' : vals,
        }
    return agg


def print_cv_summary(agg: dict, model_name: str, n_features: int) -> None:
    """IEEE-format CV result printout."""
    print(f'\n  [{model_name} | n_features={n_features}]')
    for metric, stats_d in agg.items():
        print(
            f'    {metric.upper():12s}: '
            f'{stats_d["mean"]:.3f} \u00b1 {stats_d["std"]:.3f} '
            f'(min={stats_d["min"]:.3f}, max={stats_d["max"]:.3f})'
        )


