"""## 4. Feature Selection: Bootstrap AUC + 95% CI (Saha et al.)

> **Note**: This section implements the Saha et al. (2018) filter-based feature scoring.  
> - ✅ Bootstrap resampling — preserved exactly  
> - ✅ FIX applied: `roc_auc_score(y_boot, X_boot.ravel())` — resolves
"""

def calculate_auc_ci_bootstrap(X_series, y_series, n_bootstraps: int = 1000):
    """
    Calculate AUC with 95% CI using bootstrap resampling.
    Follows Saha et al. (2018) methodology.

    FIX: roc_auc_score receives X_boot.ravel() to avoid shape mismatch.

    Parameters
    ----------
    X_series     : pd.Series  — single radiomic feature
    y_series     : pd.Series  — binary recurrence label
    n_bootstraps : int        — number of bootstrap iterations

    Returns
    -------
    mean_auc, ci_lower, ci_upper : float
    """
    aucs = []
    n    = len(X_series)

    for _ in range(n_bootstraps):
        indices = np.random.choice(n, size=n, replace=True)
        X_boot  = X_series.iloc[indices].values.reshape(-1, 1)
        y_boot  = y_series.iloc[indices].values

        if len(np.unique(y_boot)) > 1:
            # ── FIX: .ravel() ensures 1-D array ──────────────────────────
            auc = roc_auc_score(y_boot, X_boot.ravel())
            aucs.append(auc)

    aucs     = np.array(aucs)
    mean_auc = np.mean(aucs)
    ci_lower = np.percentile(aucs, 2.5)
    ci_upper = np.percentile(aucs, 97.5)
    return mean_auc, ci_lower, ci_upper


