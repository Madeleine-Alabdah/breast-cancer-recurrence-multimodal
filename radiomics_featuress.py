"""## 5. Correlation-Based Redundancy Removal

> **Note**: This is a **filter method** — independent of any model.  
> - ✅ Pearson correlation threshold = 0.8 — preserved exactly  
> - ✅ When |r| > threshold, feature with lower AUC is discarded  

"""

def select_non_redundant_features(
        radiomics_data: pd.DataFrame,
        auc_df: pd.DataFrame,
        correlation_threshold: float = 0.8,
        top_n: int = 30) -> list:
    """
    Select top non-redundant radiomic features using Pearson correlation.
    When |r| > threshold, the feature with the LOWER AUC is discarded.

    Parameters
    ----------
    radiomics_data        : pd.DataFrame — full radiomic feature matrix
    auc_df                : pd.DataFrame — AUC-ranked feature table
    correlation_threshold : float        — redundancy cutoff (default 0.8)
    top_n                 : int          — maximum features to return

    Returns
    -------
    selected_features : list[str]
    """
    selected_features = []
    feature_order     = auc_df['Feature'].tolist()
    corr_matrix       = radiomics_data[feature_order].corr().abs()

    for feature in feature_order:
        if len(selected_features) >= top_n:
            break

        is_redundant = False
        for sel_feat in selected_features:
            corr_val = corr_matrix.loc[feature, sel_feat]
            if corr_val > correlation_threshold:
                current_auc  = auc_df.loc[auc_df['Feature'] == feature,  'AUC'].values[0]
                selected_auc = auc_df.loc[auc_df['Feature'] == sel_feat, 'AUC'].values[0]
                if current_auc < selected_auc:
                    is_redundant = True
                    break

        if not is_redundant:
            selected_features.append(feature)

    return selected_features


