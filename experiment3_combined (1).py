def plot_roc_curves(predictions):
    """
    Plot ROC curves for all models.
    """

    fig, ax = plt.subplots(figsize=(10, 8))
    colors = {'Logistic Regression': '#1f77b4', 'Random Forest': '#ff7f0e', 'XGBoost': '#2ca02c'}

    for model_name, preds in predictions.items():
        y_test = preds['y_test']
        y_proba = preds['y_proba']

        fpr, tpr, _ = roc_curve(y_test, y_proba)
        roc_auc = auc(fpr, tpr)

        ax.plot(fpr, tpr, color=colors.get(model_name, '#000'), lw=2.5,
               label=f'{model_name} (AUC = {roc_auc:.3f})')

    ax.plot([0, 1], [0, 1], 'k--', lw=2, label='Random Classifier (AUC = 0.5)')

    ax.set_xlabel('False Positive Rate', fontsize=12, fontweight='bold')
    ax.set_ylabel('True Positive Rate', fontsize=12, fontweight='bold')
    ax.set_title('ROC Curves - Model Comparison', fontsize=14, fontweight='bold')
    ax.legend(fontsize=11, loc='lower right')
    ax.grid(alpha=0.3)
    ax.set_xlim([0.0, 1.0])
    ax.set_ylim([0.0, 1.05])

    plt.tight_layout()
    plt.savefig('roc_curves.png', dpi=300, bbox_inches='tight')
    plt.close()
    print("✓ Saved: roc_curves.png")


