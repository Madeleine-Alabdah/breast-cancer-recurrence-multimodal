def plot_feature_importance(trained_models, feature_names):
    """
    Plot feature importance from tree-based models.
    """

    importance_dict = {}

    if 'Random Forest' in trained_models:
        rf_model = trained_models['Random Forest']
        rf_importance = pd.DataFrame({
            'Feature': feature_names,
            'Importance': rf_model.feature_importances_
        }).sort_values('Importance', ascending=False).head(15)
        importance_dict['Random Forest'] = rf_importance

    if 'XGBoost' in trained_models:
        xgb_model = trained_models['XGBoost']
        xgb_importance = pd.DataFrame({
            'Feature': feature_names,
            'Importance': xgb_model.feature_importances_
        }).sort_values('Importance', ascending=False).head(15)
        importance_dict['XGBoost'] = xgb_importance

    if not importance_dict:
        print("No tree-based models for feature importance.")
        return

    n_models = len(importance_dict)
    fig, axes = plt.subplots(1, n_models, figsize=(8*n_models, 6))

    if n_models == 1:
        axes = [axes]

    for ax, (model_name, importance_df) in zip(axes, importance_dict.items()):
        importance_df_sorted = importance_df.sort_values('Importance')
        ax.barh(importance_df_sorted['Feature'], importance_df_sorted['Importance'], color='steelblue')
        ax.set_xlabel('Importance', fontweight='bold')
        ax.set_title(f'{model_name}\nTop 15 Features', fontweight='bold', fontsize=12)
        ax.grid(axis='x', alpha=0.3)

    plt.tight_layout()
    plt.savefig('feature_importance.png', dpi=300, bbox_inches='tight')
    plt.close()
    print("✓ Saved: feature_importance.png")



