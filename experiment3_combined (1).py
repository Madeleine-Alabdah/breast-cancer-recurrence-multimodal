def merge_two_files(clinical_df, radiomics_df, patient_id_col='patient_id'):
    """
    Merge clinical and radiomics files by patient_id.

    Args:
        clinical_df: Clinical features DataFrame
        radiomics_df: Radiomics features DataFrame
        patient_id_col: Column name for patient ID

    Returns:
        merged_df: Merged DataFrame
    """

    print("\n" + "="*70)
    print("STEP 2: MERGING FILES BY PATIENT_ID")
    print("="*70)

    print(f"\n✓ Clinical: {clinical_df.shape[0]} samples, {clinical_df.shape[1]} columns")
    print(f"✓ Radiomics: {radiomics_df.shape[0]} samples, {radiomics_df.shape[1]} columns")

    # Merge by patient_id (inner join)
    merged_df = clinical_df.merge(
        radiomics_df,
        on=patient_id_col,
        how='inner'
    )

    print(f"\n✓ After merge: {merged_df.shape[0]} samples, {merged_df.shape[1]} columns")
    print(f"✓ Total features (including patient_id): {merged_df.shape[1]}")
    print(f"✓ Total features (excluding patient_id): {merged_df.shape[1] - 1}")

    # Check for missing values
    missing_count = merged_df.isna().sum().sum()
    print(f"✓ Missing values in merged data: {missing_count}")

    return merged_df


