def load_two_files(file_clinical, file_radiomics):
    """
    Load your 2 input files.

    Args:
        file_clinical: Path to clinical features file (CSV or Excel)
        file_radiomics: Path to radiomics features file (CSV or Excel)

    Returns:
        clinical_df: Clinical features DataFrame
        radiomics_df: Radiomics features DataFrame
    """

    print("="*70)
    print("STEP 1: LOADING 2 FILES")
    print("="*70)

    # Load clinical file
    if file_clinical.endswith('.xlsx') or file_clinical.endswith('.xls'):
        clinical_df = pd.read_excel(file_clinical)
    else:
        clinical_df = pd.read_csv(file_clinical)

    print(f"\n✓ Clinical file loaded: {clinical_df.shape}")
    print(f"  Columns: {clinical_df.columns.tolist()}")

    # Load radiomics file
    if file_radiomics.endswith('.xlsx') or file_radiomics.endswith('.xls'):
        radiomics_df = pd.read_excel(file_radiomics)
    else:
        radiomics_df = pd.read_csv(file_radiomics)

    print(f"\n✓ Radiomics file loaded: {radiomics_df.shape}")
    print(f"  Columns: {radiomics_df.columns.tolist()}")

    return clinical_df, radiomics_df


