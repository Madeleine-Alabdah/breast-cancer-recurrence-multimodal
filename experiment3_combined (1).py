def optimize_threshold(y_true, y_proba, model_name="Model"):
    """
    Find optimal threshold using F1-score maximization.

    Args:
        y_true: True labels
        y_proba: Predicted probabilities
        model_name: Model name for display

    Returns:
        optimal_threshold: Best threshold value
        metrics_df: DataFrame with all threshold metrics
    """

    thresholds = np.arange(0.05, 0.96, 0.05)
    results = []

    for t in thresholds:
        y_pred = (y_proba >= t).astype(int)

        tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()

        sensitivity = tp / (tp + fn) if (tp + fn) > 0 else 0
        specificity = tn / (tn + fp) if (tn + fp) > 0 else 0
        precision = tp / (tp + fp) if (tp + fp) > 0 else 0
        f1 = f1_score(y_true, y_pred, zero_division=0)
        accuracy = accuracy_score(y_true, y_pred)

        results.append({
            'Threshold': round(t, 2),
            'Sensitivity': sensitivity,
            'Specificity': specificity,
            'Precision': precision,
            'F1': f1,
            'Accuracy': accuracy,
        })

    metrics_df = pd.DataFrame(results)
    optimal_idx = metrics_df['F1'].idxmax()
    optimal_threshold = metrics_df.loc[optimal_idx, 'Threshold']

    print(f"{model_name}: τ* = {optimal_threshold:.2f} (F1 = {metrics_df.loc[optimal_idx, 'F1']:.4f})")

    return optimal_threshold, metrics_df


