def prepare_features_target(merged_df, target_col='recurrence_event_s', patient_id_col='patient_id'):
    """
    Separate features from target variable.

    Args:
        merged_df: Merged DataFrame
        target_col: Name of target column (recurrence yes/no)
        patient_id_col: Name of patient ID column

    Returns:
        X: Feature matrix
        y: Target variable
        patient_ids: Patient IDs
    """

    print("\n" + "="*70)
    print("STEP 3: PREPARING FEATURES AND TARGET")
    print("="*70)

    # Get patient IDs
    patient_ids = merged_df[patient_id_col].copy()

    # Get target
    y = merged_df[target_col].copy()
    print(f"\n✓ Target column: {target_col}")
    print(f"  Distribution: {y.value_counts().to_dict()}")

    # Get features (all except patient_id and target)
    X = merged_df.drop(columns=[patient_id_col, target_col])
    print(f"\n✓ Features: {X.shape[1]}")
    print(f"✓ Samples: {X.shape[0]}")

    # Handle missing values
    missing_per_feature = X.isna().sum()
    features_with_missing = missing_per_feature[missing_per_feature > 0]

    if len(features_with_missing) > 0:
        print(f"\n⚠️  Features with missing values:")
        print(features_with_missing)

        # Fill missing values with median
        X = X.fillna(X.median())
        print(f"\n✓ Missing values filled with median")
    else:
        print(f"\n✓ No missing values")

    return X, y, patient_ids


