# STEP 6: EVALUATE MODELS
# ============================================================================

def evaluate_model(y_true, y_pred, y_proba, model_name):
    """
    Calculate comprehensive evaluation metrics.

    Args:
        y_true: True labels
        y_pred: Predicted labels (using optimal threshold)
        y_proba: Predicted probabilities
        model_name: Model name

    Returns:
        Dictionary with all metrics
    """

    accuracy = accuracy_score(y_true, y_pred)
    tn, fp, fn, tp = confusion_matrix(y_true, y_pred).ravel()

    sensitivity = tp / (tp + fn) if (tp + fn) > 0 else 0
    specificity = tn / (tn + fp) if (tn + fp) > 0 else 0
    precision = tp / (tp + fp) if (tp + fp) > 0 else 0
    f1 = f1_score(y_true, y_pred, zero_division=0)
    roc_auc = roc_auc_score(y_true, y_proba)

    # Fisher's exact test p-value
    oddsratio, pvalue = stats.fisher_exact([[tp, fp], [fn, tn]])

    return {
        'Model': model_name,
        'Accuracy': accuracy,
        'Sensitivity': sensitivity,
        'Specificity': specificity,
        'Precision': precision,
        'F1-Score': f1,
        'ROC-AUC': roc_auc,
        'P-Value': pvalue
    }


def create_results_table(trained_models, predictions, optimal_thresholds):
    """
    Create final results summary table.

    Args:
        trained_models: Trained model objects
        predictions: Predictions dictionary
        optimal_thresholds: Optimal threshold per model

    Returns:
        results_df: Results DataFrame
    """

    results_list = []

    for model_name in trained_models.keys():
        if model_name not in predictions:
            continue

        preds = predictions[model_name]
        y_test = preds['y_test']
        y_proba = preds['y_proba']

        opt_threshold = optimal_thresholds.get(model_name, 0.5)
        y_pred = (y_proba >= opt_threshold).astype(int)

        metrics = evaluate_model(y_test, y_pred, y_proba, model_name)
        metrics['Optimal_Threshold'] = opt_threshold

        results_list.append(metrics)

    results_df = pd.DataFrame(results_list)

    col_order = ['Model', 'Optimal_Threshold', 'Accuracy', 'Sensitivity', 'Specificity',
                 'Precision', 'F1-Score', 'ROC-AUC', 'P-Value']
    results_df = results_df[[c for c in col_order if c in results_df.columns]]

    return results_df


