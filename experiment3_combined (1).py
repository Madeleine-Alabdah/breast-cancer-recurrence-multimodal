def get_top_features(trained_models, feature_names, top_k=10):
    """
    Extract top features from each trained model.

    Args:
        trained_models: dictionary of trained models
        feature_names: list of feature names
        top_k: number of features per model

    Returns:
        selected_features: list of unique selected features
    """

    selected_features = set()

    # Logistic Regression
    if 'Logistic Regression' in trained_models:
        lr = trained_models['Logistic Regression']
        importance = np.abs(lr.coef_[0])

        lr_df = pd.DataFrame({
            "Feature": feature_names,
            "Importance": importance
        }).sort_values("Importance", ascending=False).head(top_k)

        selected_features.update(lr_df["Feature"].tolist())

    # Random Forest
    if 'Random Forest' in trained_models:
        rf = trained_models['Random Forest']

        rf_df = pd.DataFrame({
            "Feature": feature_names,
            "Importance": rf.feature_importances_
        }).sort_values("Importance", ascending=False).head(top_k)

        selected_features.update(rf_df["Feature"].tolist())

    # XGBoost
    if 'XGBoost' in trained_models:
        xgb = trained_models['XGBoost']

        xgb_df = pd.DataFrame({
            "Feature": feature_names,
            "Importance": xgb.feature_importances_
        }).sort_values("Importance", ascending=False).head(top_k)

        selected_features.update(xgb_df["Feature"].tolist())

    selected_features = list(selected_features)

    print("\n✓ Selected features:", len(selected_features))
    print(selected_features)

    return selected_features

